def set_user_language(user_id: int, users_db: Dict, language: str) -> bool:
    """
    –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ—Ç —è–∑—ã–∫ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
    
    Args:
        user_id: ID –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        users_db: –ë–∞–∑–∞ –¥–∞–Ω–Ω—ã—Ö –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π
        language: –ö–æ–¥ —è–∑—ã–∫–∞
    
    Returns:
        True –µ—Å–ª–∏ —É—Å–ø–µ—à–Ω–æ, False –µ—Å–ª–∏ —è–∑—ã–∫ –Ω–µ –ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ—Ç—Å—è
    """
    if language not in LANGUAGES:
        return False
    
    if user_id not in users_db:
        users_db[user_id] = {}
    
    users_db[user_id]['language'] = language
    return True


def get_language_keyboard():
    """
    –°–æ–∑–¥–∞–µ—Ç –∫–ª–∞–≤–∏–∞—Ç—É—Ä—É –≤—ã–±–æ—Ä–∞ —è–∑—ã–∫–∞
    
    Returns:
        List[List[str]]: –°–ø–∏—Å–æ–∫ –∫–Ω–æ–ø–æ–∫ –¥–ª—è InlineKeyboardMarkup
    """
    from telegram import InlineKeyboardButton
    
    buttons = []
    for lang_code, lang_info in LANGUAGES.items():
        button_text = f"{lang_info['flag']} {lang_info['name']}"
        buttons.append([InlineKeyboardButton(button_text, callback_data=f"lang_{lang_code}")])
    
    return buttons





